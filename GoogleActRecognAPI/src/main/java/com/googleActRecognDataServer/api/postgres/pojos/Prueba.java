package com.googleActRecognDataServer.api.postgres.pojos;

public class Prueba {
	private String nombre;
	private int edad;
	
	public Prueba(String nombre, int edad) {
		this.nombre = nombre;
		this.edad = edad;
	}

	public String getNombre() {
		return nombre;
	}

	public int getEdad() {
		return edad;
	}
	
	

}
